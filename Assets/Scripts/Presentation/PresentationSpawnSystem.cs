    void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }
        Instance = this;

        // Create fallback primitives
        _fallbackUnitPrefab = CreateFallbackPrefab("FallbackUnit", PrimitiveType.Capsule, 0.5f);
        _fallbackBuildingPrefab = CreateFallbackPrefab("FallbackBuilding", PrimitiveType.Cube, 2f);
    }

    void Start()
    {
        _world = Unity.Entities.World.DefaultGameObjectInjectionWorld;
        if (_world != null && _world.IsCreated)
        {
            _em = _world.EntityManager;
            _presentationQuery = _em.CreateEntityQuery(
                ComponentType.ReadOnly<PresentationId>(),
                ComponentType.ReadOnly<LocalTransform>()
            );
        }
    }

    void Update()
    {
        if (_world == null || !_world.IsCreated) return;

        CleanupDestroyedEntities();
        SpawnMissingVisuals();
        SyncTransforms();
    }

    /// <summary>
    /// Detects ECS entities that have been destroyed (e.g., by DeathSystem)
    /// and cleans up their corresponding GameObjects.
    /// </summary>
    private void CleanupDestroyedEntities()
    {
        if (EntityViewManager.Instance == null) return;

        // Collect stale entities (can't modify HashSet during iteration)
        var toRemove = new System.Collections.Generic.List<Entity>();

        foreach (var entity in _spawnedEntities)
        {
            if (!_em.Exists(entity))
            {
                toRemove.Add(entity);
            }
        }

        foreach (var entity in toRemove)
        {
            _spawnedEntities.Remove(entity);

            if (EntityViewManager.Instance.TryGetView(entity, out var go))
            {
                EntityViewManager.Instance.UnregisterView(entity);
                if (go != null) Destroy(go);
            }
        }
    }

    private void SpawnMissingVisuals()
    {
        if (_presentationQuery == null) return;

        var entities = _presentationQuery.ToEntityArray(Unity.Collections.Allocator.Temp);
        var presentations = _presentationQuery.ToComponentDataArray<PresentationId>(Unity.Collections.Allocator.Temp);
        var transforms = _presentationQuery.ToComponentDataArray<LocalTransform>(Unity.Collections.Allocator.Temp);

        for (int i = 0; i < entities.Length; i++)
        {
            var entity = entities[i];

            // Skip if already spawned
            if (_spawnedEntities.Contains(entity)) continue;

            // Skip if EntityViewManager already has it
            if (EntityViewManager.Instance != null &&
                EntityViewManager.Instance.TryGetView(entity, out _))
            {
                _spawnedEntities.Add(entity);
                continue;
            }

            var presentationId = presentations[i].Id;
            var transform = transforms[i];

            // Spawn the visual
            var go = SpawnVisual(entity, presentationId, transform);
            if (go != null)
            {
                // Register with EntityViewManager
                if (EntityViewManager.Instance != null)
                    EntityViewManager.Instance.RegisterView(entity, go);

                _spawnedEntities.Add(entity);

            }
        }

        entities.Dispose();
        presentations.Dispose();
        transforms.Dispose();
    }

    private GameObject SpawnVisual(Entity entity, int presentationId, LocalTransform transform)
    {
        // Get position and adjust Y to terrain height using shared utility
        Vector3 pos = transform.Position;
        pos.y = TerrainUtility.GetHeight(pos.x, pos.z);

        // === PROCEDURAL OBSTACLES: generate compound GameObjects instead of loading prefabs ===
        if (presentationId == ObstacleBootstrap.ForestPresentationId)
        {
            float radius = _em.HasComponent<Radius>(entity) ? _em.GetComponentData<Radius>(entity).Value : 5f;
            var go = CreateProceduralForest(pos, radius, entity);
            return go;
        }
        if (presentationId == ObstacleBootstrap.RockPresentationId)
        {
            float radius = _em.HasComponent<Radius>(entity) ? _em.GetComponentData<Radius>(entity).Value : 3f;
            var go = CreateProceduralRockFormation(pos, radius, entity);
            return go;
        }
        if (presentationId == IronDepositBootstrap.IronDepositPresentationId)
        {
            var go = CreateProceduralIronDeposit(pos, entity);
            return go;
        }

        // === ALANTHOR WALLS: procedural hub, instances, towers, gates ===
        if (presentationId == TheWaningBorder.Entities.AlanthorWall.HubPresentationID)
        {
            var go = CreateProceduralWallHub(pos, entity);
            return go;
        }
        if (presentationId == 551) // Legacy segment — kept for backward compat
        {
            var go = CreateProceduralWallSegment(pos, entity);
            return go;
        }
        if (presentationId == TheWaningBorder.Entities.AlanthorWall.InstancePresentationID)
        {
            var go = CreateProceduralWallInstance(pos, entity);
            return go;
        }
        if (presentationId == TheWaningBorder.Entities.AlanthorWall.TowerPresentationID)
        {
            var go = CreateProceduralWallTower(pos, entity);
            return go;
        }
        if (presentationId == TheWaningBorder.Entities.AlanthorWall.GatePresentationID)
        {
            var go = CreateProceduralWallGate(pos, entity);
            return go;
        }

        // === ALANTHOR SMELTER: procedural forge building ===
        if (presentationId == TheWaningBorder.Entities.Smelter.PresentationID)
        {
            var go = CreateProceduralSmelter(pos, entity);
            return go;
        }

        // Hall (100), Hut (102) and Barracks (510) are handled by the
        // culture-aware ProceduralBuildingGenerator dispatcher below — see
        // CreateHall / CreateHut / CreateBarracks there. (The hand-authored
        // *_base prefab path is shelved for now; revert here when restoring.)

        // Vault (530) now goes through the culture-aware ProceduralBuildingGenerator
        // dispatcher below — see CreateVault there. The local CreateProceduralVault
        // is now dead code (kept for reference, may be removed in a later cleanup).

        // === CRYSTAL CURSE: tiles are DPS markers only — visual is painted
        // once per node by CrystalSpreadSystem as an organic growing blob ===
        if (presentationId == CursedGroundPresentationId)
        {
            // Return a minimal hidden root so PresentationSpawnSystem tracks this entity
            // (needed for cleanup when entity is destroyed, e.g. crystal node killed)
            var go = new GameObject($"CursedGround_{entity.Index}");
            go.transform.position = pos;
            go.SetActive(false); // Invisible — terrain painting is the visual
            return go;
        }

        // === SOLDIER UNIT: procedural humanoid from primitives ===
        if (presentationId == 201) // Swordsman
        {
            var go = SoldierModelBuilder.Create(pos, entity);
            ApplyFactionColor(go, entity);
            return go;
        }

        // === CRYSTAL LOOT PILE (cadaver): procedural crystal cluster on the ground ===
        if (presentationId == 301)
        {
            var go = CreateProceduralCadaverLoot(pos, entity);
            return go;
        }

        // === CRYSTAL NODES (buildings): procedural crystal-themed visuals ===
        // Crystal UNITS (320-322) use actual prefabs, so they fall through to prefab loading below
        if (presentationId >= 310 && presentationId <= 316 && presentationId != 311)
        {
            var go = CreateProceduralCrystalEntity(pos, presentationId, entity);
            return go;
        }

        // === PROCEDURAL BUILDINGS: culture-aware generated visuals ===
        // Determine faction culture for building tone
        byte buildingCulture = Cultures.None;
        if (_em.HasComponent<FactionTag>(entity))
        {
            var faction = _em.GetComponentData<FactionTag>(entity).Value;
            buildingCulture = FactionColors.GetFactionCulture(faction);
        }

        // Handle PresentationId 355 which is shared between Runai TradingPost and Alanthor Garrison
        if (presentationId == 355)
        {
            bool isAlanthor = _em.HasComponent<GarrisonTag>(entity);
            var go355 = ProceduralBuildingGenerator.Create355(pos, entity, isAlanthor);
            if (go355 != null) return FinishProceduralBuilding(go355, entity, transform);
        }

        // === BASE BUILDING PREFABS (Hall / Barracks / Hut at level 0) ===
        // Prefer hand-authored prefabs at Resources/Prefabs/Buildings/{name}
        // when present; fall through to procedural otherwise. The level-1+
        // visuals (Hall_al_1, …) are managed separately by
        // BuildingPrefabSwapSystem after upgrades complete.
        {
            var basePrefab = TryLoadBaseBuildingPrefab(presentationId);
            if (basePrefab != null)
            {
                // FinishProceduralBuilding writes the final visual rotation
                // (with the +180° building offset) — we just position here.
                var go = Instantiate(basePrefab, pos, Quaternion.identity);
                go.SetActive(true);
                // Preserve the prefab's authored scale through SyncTransforms.
                var ps = basePrefab.transform.localScale;
                float baseScale = (ps.x + ps.y + ps.z) / 3f;
                if (baseScale > 0.001f)
                {
                    var tag = go.AddComponent<ProceduralScaleTag>();
                    tag.BaseScale = baseScale;
                    go.transform.localScale = Vector3.one * transform.Scale * baseScale;
                }
                return FinishProceduralBuilding(go, entity, transform);
            }
        }

        // Try procedural generation for all known building types
        {
            var procGo = ProceduralBuildingGenerator.TryCreate(presentationId, pos, entity, buildingCulture);
            if (procGo != null) return FinishProceduralBuilding(procGo, entity, transform);
        }

        // === PROCEDURAL UNITS: unique generated visuals for all unit types ===
        {
            var unitGo = ProceduralUnitGenerator.TryCreate(presentationId, pos, entity);
            if (unitGo != null) return FinishProceduralUnit(unitGo, entity, presentationId, transform);
        }


        GameObject prefab = null;

        // Culture-specific override (e.g. Alanthor Barracks) takes priority over the
        // default per-id prefab. Falls through to PrefabPaths if no override exists or
        // the override prefab fails to load.
        if (CulturePrefabOverrides.TryGetValue((presentationId, buildingCulture), out string culturePath))
        {
            prefab = Resources.Load<GameObject>(culturePath);
        }

        // Default prefab for this presentation id
        if (prefab == null && PrefabPaths.TryGetValue(presentationId, out string path))
        {
            prefab = Resources.Load<GameObject>(path);
        }

        // Fallback based on ID range
        // Units: 200-499, Buildings: 100-199 and 500+
        if (prefab == null)
        {
            if (presentationId >= 200 && presentationId < 500)
                prefab = _fallbackUnitPrefab;
            else
                prefab = _fallbackBuildingPrefab;
        }

        if (prefab == null) return null;

        var goInst = Instantiate(prefab);
        goInst.SetActive(true); // Ensure active (fallback prefabs are stored inactive)
        goInst.name = $"Entity_{entity.Index}_{presentationId}";
        goInst.transform.position = pos;
        goInst.transform.rotation = VisualRotation(entity, transform.Rotation);
        goInst.transform.localScale = Vector3.one * transform.Scale;

        // Ensure a collider exists for raycasting/selection
        if (goInst.GetComponentInChildren<Collider>() == null)
        {
            var col = goInst.AddComponent<BoxCollider>();
            // Size collider based on entity radius
            float radius = 0.5f;
            if (_em.HasComponent<Radius>(entity))
                radius = _em.GetComponentData<Radius>(entity).Value;
            col.size = Vector3.one * radius * 2f;
        }

        // Add EntityReference for raycasting/selection
        var entityRef = goInst.GetComponent<EntityReference>();
        if (entityRef == null)
            entityRef = goInst.AddComponent<EntityReference>();
        entityRef.Entity = entity;

        // Attach VeilstingerGunTracker for Veilstinger units (PresentationId 321)
        // This rotates leftgun/rightgun children to LookAt their respective targets
        if (presentationId == 321)
        {
            var gunTracker = goInst.AddComponent<VeilstingerGunTracker>();
            gunTracker.Entity = entity;
        }

        // Attach UnitAnimationSync for units that have an Animator
        // (units have MoveSpeed, buildings don't)
        if (_em.HasComponent<MoveSpeed>(entity))
        {
            var animator = goInst.GetComponentInChildren<Animator>();
            if (animator != null)
            {
                var animSync = goInst.AddComponent<UnitAnimationSync>();
                animSync.LinkedEntity = entity;
            }
        }

        // Apply faction coloring
        ApplyFactionColor(goInst, entity);

        return goInst;
    }

    private void ApplyFactionColor(GameObject go, Entity entity)
    {
        if (!_em.HasComponent<FactionTag>(entity)) return;

        var faction = _em.GetComponentData<FactionTag>(entity).Value;
        var color = FactionColors.Get(faction); // Returns culture color if faction has aged up

        bool isBuilding = _em.HasComponent<BuildingTag>(entity);

        if (isBuilding)
        {
            // Buildings: color roof parts and stripe materials for faction ownership
            foreach (var renderer in go.GetComponentsInChildren<Renderer>())
            {
                // Tint roof objects with faction color (preserves texture detail)
                bool isRoof = renderer.gameObject.name.IndexOf("roof",
                    System.StringComparison.OrdinalIgnoreCase) >= 0;
                bool isStripe = renderer.gameObject.name.IndexOf("stripe",
                    System.StringComparison.OrdinalIgnoreCase) >= 0;

                foreach (var mat in renderer.materials)
                {
                    if (isRoof)
                    {
                        // Replace roof texture with white so faction color is solid
                        if (mat.HasProperty("_BaseMap"))
                            mat.SetTexture("_BaseMap", Texture2D.whiteTexture);
                        if (mat.HasProperty("_MainTex"))
                            mat.SetTexture("_MainTex", Texture2D.whiteTexture);

                        if (mat.HasProperty("_BaseColor"))
                            mat.SetColor("_BaseColor", color);
                        else if (mat.HasProperty("_Color"))
                            mat.color = color;
                    }

                    // Faction-colored stripes on canvas walls (procedural buildings)
                    if (isStripe)
                    {
                        if (mat.HasProperty("_BaseColor"))
                            mat.SetColor("_BaseColor", color);
                        else if (mat.HasProperty("_Color"))
                            mat.color = color;
                    }

                    if (mat.HasProperty("_StripeColor"))
                        mat.SetColor("_StripeColor", color);
                }
            }
            AddFactionBanner(go, color);
        }
        else
        {
            // Units: check if any renderer has "faction" in the name (procedural models)
            // If so, only color those accent parts; otherwise color all materials (legacy prefabs)
            bool hasFactionParts = false;
            foreach (var renderer in go.GetComponentsInChildren<Renderer>())
            {
                if (renderer.gameObject.name.IndexOf("faction",
                    System.StringComparison.OrdinalIgnoreCase) >= 0)
                {
                    hasFactionParts = true;
                    break;
                }
            }

            foreach (var renderer in go.GetComponentsInChildren<Renderer>())
            {
                // If model has dedicated faction parts, only color those
                if (hasFactionParts &&
                    renderer.gameObject.name.IndexOf("faction",
                        System.StringComparison.OrdinalIgnoreCase) < 0)
                    continue;

                foreach (var mat in renderer.materials)
                {
                    if (mat.HasProperty("_Color"))
                        mat.color = color;
                    if (mat.HasProperty("_BaseColor"))
                        mat.SetColor("_BaseColor", color);
                    if (mat.HasProperty("_StripeColor"))
                        mat.SetColor("_StripeColor", color);
                }
            }
        }
    }

    /// <summary>
    /// Finish setting up a procedurally generated building: collider, EntityReference,
    /// transform sync, and faction coloring. Mirrors the setup done for prefab-based buildings.
    /// </summary>
    private GameObject FinishProceduralBuilding(GameObject go, Entity entity, LocalTransform transform)
    {
        go.name = $"Entity_{entity.Index}_{(_em.HasComponent<PresentationId>(entity) ? _em.GetComponentData<PresentationId>(entity).Id : 0)}";
        go.transform.rotation = VisualRotation(entity, transform.Rotation);
        go.transform.localScale = Vector3.one * transform.Scale;

        // Add aggregate collider sized to actual visual bounds (not entity Radius).
        // Note: procedural primitives have their own colliders destroyed via
        // Object.Destroy by Prim helpers, but those destroyed colliders remain
        // queryable in the same frame — using GetComponentInChildren here would
        // false-positive on them and skip adding the aggregate. Procedural
        // buildings never place a collider directly on the root, so checking the
        // root alone is the correct guard.
        if (go.GetComponent<Collider>() == null)
        {
            var col = go.AddComponent<BoxCollider>();

            // Compute enclosing bounds from all child renderers (these are valid
            // even though their gameobjects' colliders are pending destruction).
            var renderers = go.GetComponentsInChildren<Renderer>();
            if (renderers.Length > 0)
            {
                var bounds = renderers[0].bounds;
                for (int r = 1; r < renderers.Length; r++)
                    bounds.Encapsulate(renderers[r].bounds);

                // Convert world bounds to local space
                col.center = go.transform.InverseTransformPoint(bounds.center);
                col.size = bounds.size; // already in world scale, local scale is 1
            }
            else
            {
                // Fallback if no renderers
                float radius = 0.5f;
                if (_em.HasComponent<Radius>(entity))
                    radius = _em.GetComponentData<Radius>(entity).Value;
                col.size = Vector3.one * radius * 2f;
                col.center = Vector3.up * radius;
            }
        }

        // EntityReference for raycasting/selection
        var entityRef = go.GetComponent<EntityReference>();
        if (entityRef == null) entityRef = go.AddComponent<EntityReference>();
        entityRef.Entity = entity;

        // Faction banner (procedural buildings already have culture tone baked in,
        // but still need the faction banner for player identity)
        ApplyFactionBannerOnly(go, entity);

        return go;
    }

