    // Cached Resources lookups so we don't re-resolve every spawn. null
    // entries mean "negative cache — no prefab exists at this path".
    private readonly System.Collections.Generic.Dictionary<int, GameObject> _baseBuildingPrefabCache = new();
    private readonly System.Collections.Generic.HashSet<int> _baseBuildingPrefabNegativeCache = new();

    /// <summary>
    /// Look up the level-0 hand-authored prefab for Hall / Barracks / Hut.
    /// Returns null if no prefab is present at the canonical path — caller
    /// falls back to procedural generation.
    /// </summary>
    private GameObject TryLoadBaseBuildingPrefab(int presentationId)
    {
        if (_baseBuildingPrefabCache.TryGetValue(presentationId, out var cached))
            return cached;
        if (_baseBuildingPrefabNegativeCache.Contains(presentationId)) return null;

        string path = presentationId switch
        {
            100 => "Prefabs/Buildings/Hall",     // Hall.PresentationID
            102 => "Prefabs/Buildings/Hut",      // Hut.PresentationID
            510 => "Prefabs/Buildings/Barracks", // Barracks.PresentationID
            _   => null,
        };
        if (path == null)
        {
            _baseBuildingPrefabNegativeCache.Add(presentationId);
            return null;
        }

        var loaded = Resources.Load<GameObject>(path);
        if (loaded == null)
        {
            _baseBuildingPrefabNegativeCache.Add(presentationId);
            return null;
        }
        _baseBuildingPrefabCache[presentationId] = loaded;
        return loaded;
    }

    /// <summary>
    /// Finish setting up a procedurally generated unit: collider, EntityReference,
    /// transform sync, and faction coloring.
    /// </summary>
    private GameObject FinishProceduralUnit(GameObject go, Entity entity, int presentationId, LocalTransform transform)
    {
        go.name = $"Entity_{entity.Index}_{presentationId}";
        go.transform.rotation = transform.Rotation;

        // Apply ECS scale multiplied by procedural base scale
        var scaleTag = go.GetComponent<ProceduralScaleTag>();
        float baseScale = (scaleTag != null) ? scaleTag.BaseScale : 1f;
        go.transform.localScale = Vector3.one * transform.Scale * baseScale;

        // Add collider for raycasting/selection (small box for units)
        if (go.GetComponentInChildren<Collider>() == null)
        {
            var col = go.AddComponent<BoxCollider>();
            float radius = 0.3f;
            if (_em.HasComponent<Radius>(entity))
                radius = _em.GetComponentData<Radius>(entity).Value;
            col.size = new Vector3(radius * 2f, 1f, radius * 2f);
            col.center = Vector3.up * 0.5f;
        }

        // EntityReference for raycasting/selection
        var entityRef = go.GetComponent<EntityReference>();
        if (entityRef == null) entityRef = go.AddComponent<EntityReference>();
        entityRef.Entity = entity;

        // Attach VeilstingerGunTracker for Veilstinger units
        if (presentationId == 321)
        {
            var gunTracker = go.AddComponent<VeilstingerGunTracker>();
            gunTracker.Entity = entity;
        }

        // Apply faction coloring (overwrites white/neutral parts with faction color)
        ApplyFactionColor(go, entity);

        return go;
    }

    /// <summary>
    /// Apply only the faction banner to a procedural building (walls already have culture tone).
    /// </summary>
    private void ApplyFactionBannerOnly(GameObject go, Entity entity)
    {
        if (!_em.HasComponent<FactionTag>(entity)) return;
        var faction = _em.GetComponentData<FactionTag>(entity).Value;
        var color = FactionColors.Get(faction);
        AddFactionBanner(go, color);
    }

    /// <summary>
    /// Adds a small colored banner on top of a building for faction ownership visibility.
    /// </summary>
    private void AddFactionBanner(GameObject building, Color color)
    {
        // Skip if banner already exists
        if (building.transform.Find("FactionBanner") != null) return;

        // Find the highest point of the building for banner placement
        float maxY = 0f;
        foreach (var renderer in building.GetComponentsInChildren<Renderer>())
        {
            float top = renderer.bounds.max.y - building.transform.position.y;
            if (top > maxY) maxY = top;
        }

        // Create banner pole + flag
        var banner = new GameObject("FactionBanner");
        banner.transform.SetParent(building.transform, false);
        banner.transform.localPosition = new Vector3(0f, maxY + 0.1f, 0f);

        // Pole
        var pole = GameObject.CreatePrimitive(PrimitiveType.Cylinder);
        pole.name = "Pole";
        pole.transform.SetParent(banner.transform, false);
        pole.transform.localPosition = new Vector3(0f, 0.5f, 0f);
        pole.transform.localScale = new Vector3(0.05f, 0.5f, 0.05f);
        Object.Destroy(pole.GetComponent<Collider>());
        pole.GetComponent<Renderer>().material.color = new Color(0.3f, 0.25f, 0.2f); // Dark wood

        // Flag
        var flag = GameObject.CreatePrimitive(PrimitiveType.Cube);
        flag.name = "Flag";
        flag.transform.SetParent(banner.transform, false);
        flag.transform.localPosition = new Vector3(0.2f, 0.85f, 0f);
        flag.transform.localScale = new Vector3(0.35f, 0.2f, 0.04f);
        Object.Destroy(flag.GetComponent<Collider>());
        var flagMat = flag.GetComponent<Renderer>().material;
        flagMat.color = color;
        if (flagMat.HasProperty("_BaseColor"))
            flagMat.SetColor("_BaseColor", color);
    }

    /// <summary>
    /// Reapply faction colors to all visuals belonging to a faction.
    /// Called after age-up to refresh culture colors on existing entities.
    /// </summary>
    public void RefreshFactionColors(Faction faction)
    {
        if (EntityViewManager.Instance == null) return;

        var entities = _presentationQuery.ToEntityArray(Unity.Collections.Allocator.Temp);

        for (int i = 0; i < entities.Length; i++)
        {
            var entity = entities[i];
            if (!_em.HasComponent<FactionTag>(entity)) continue;
            if (_em.GetComponentData<FactionTag>(entity).Value != faction) continue;

            if (EntityViewManager.Instance.TryGetView(entity, out var go) && go != null)
            {
                ApplyFactionColor(go, entity);
            }
        }

        entities.Dispose();
    }

    /// <summary>
    /// Rebuild all building visuals for a faction with culture-aware procedural generation.
    /// Called after age-up completion to retrofit existing Era 1 buildings with culture tone.
    /// </summary>
    public void RefreshFactionVisuals(Faction faction)
    {
        if (EntityViewManager.Instance == null) return;

        byte culture = FactionColors.GetFactionCulture(faction);

        var entities = _presentationQuery.ToEntityArray(Unity.Collections.Allocator.Temp);
        var presentations = _presentationQuery.ToComponentDataArray<PresentationId>(Unity.Collections.Allocator.Temp);
        var transforms = _presentationQuery.ToComponentDataArray<LocalTransform>(Unity.Collections.Allocator.Temp);

        for (int i = 0; i < entities.Length; i++)
        {
            var entity = entities[i];
            if (!_em.HasComponent<FactionTag>(entity)) continue;
            if (_em.GetComponentData<FactionTag>(entity).Value != faction) continue;
            if (!_em.HasComponent<BuildingTag>(entity)) continue;

            int pid = presentations[i].Id;

            // A culture-aware rebuild is needed when the building has either a
            // procedural variant (ProceduralBuildingGenerator handles it) or a
            // culture-specific prefab override (e.g. Alanthor Barracks).
            bool hasProceduralVariant;
            {
                var testGo = ProceduralBuildingGenerator.TryCreate(pid, Vector3.zero, entity, culture);
                hasProceduralVariant = testGo != null;
                if (testGo != null) Destroy(testGo);
            }
            bool hasCultureOverride = CulturePrefabOverrides.ContainsKey((pid, culture));

            if (!hasProceduralVariant && !hasCultureOverride)
            {
                // No culture-specific visual for this building — just refresh colors.
                if (EntityViewManager.Instance.TryGetView(entity, out var existGo) && existGo != null)
                    ApplyFactionColor(existGo, entity);
                continue;
            }

            // Destroy old visual
            if (EntityViewManager.Instance.TryGetView(entity, out var oldGo) && oldGo != null)
            {
                EntityViewManager.Instance.UnregisterView(entity);
                _spawnedEntities.Remove(entity);
                Destroy(oldGo);
            }

            // Respawn through the canonical entry point so procedural and
            // prefab-override paths both go through the same setup (colliders,
            // EntityReference, faction coloring).
            var newGo = SpawnVisual(entity, pid, transforms[i]);
            if (newGo != null)
            {
                EntityViewManager.Instance.RegisterView(entity, newGo);
                _spawnedEntities.Add(entity);
            }
        }

        entities.Dispose();
        presentations.Dispose();
        transforms.Dispose();

    }

    private void SyncTransforms()
    {
        if (EntityViewManager.Instance == null) return;

        var entities = _presentationQuery.ToEntityArray(Unity.Collections.Allocator.Temp);
        var transforms = _presentationQuery.ToComponentDataArray<LocalTransform>(Unity.Collections.Allocator.Temp);

        var em = World.DefaultGameObjectInjectionWorld?.EntityManager;

        for (int i = 0; i < entities.Length; i++)
        {
            if (EntityViewManager.Instance.TryGetView(entities[i], out var go) && go != null)
            {
                var pos = (Vector3)transforms[i].Position;
                pos.y = TerrainUtility.GetHeight(pos.x, pos.z);

                // Construction rising animation: buildings start below ground and rise
                if (em.HasValue && em.Value.HasComponent<UnderConstruction>(entities[i]))
                {
                    var uc = em.Value.GetComponentData<UnderConstruction>(entities[i]);
                    float ratio = uc.Total > 0 ? Mathf.Clamp01(uc.Progress / uc.Total) : 1f;
                    // Sink depth based on building radius (taller buildings sink more)
                    float sinkDepth = em.Value.HasComponent<Radius>(entities[i])
                        ? em.Value.GetComponentData<Radius>(entities[i]).Value * 2f
                        : 3f;
                    pos.y -= sinkDepth * (1f - ratio);
                }

                go.transform.position = pos;
                go.transform.rotation = VisualRotation(entities[i], transforms[i].Rotation);
                // Respect procedural unit base scale (ProceduralScaleTag)
                var scaleTag = go.GetComponent<ProceduralScaleTag>();
                float baseScale = (scaleTag != null) ? scaleTag.BaseScale : 1f;
                go.transform.localScale = Vector3.one * transforms[i].Scale * baseScale;
            }
        }

        entities.Dispose();
        transforms.Dispose();
    }

    private GameObject CreateFallbackPrefab(string name, PrimitiveType type, float scale)
    {
        var go = GameObject.CreatePrimitive(type);
        go.name = name;
        go.transform.localScale = Vector3.one * scale;
        go.SetActive(false); // Hide the template
        DontDestroyOnLoad(go);
        return go;
    }

    private Color GetFactionColor(Faction faction)
    {
        return faction switch
        {
            Faction.Blue => new Color(0.3f, 0.5f, 1f),
            Faction.Red => new Color(1f, 0.3f, 0.3f),
            Faction.Green => new Color(0.3f, 1f, 0.3f),
            Faction.Yellow => new Color(1f, 1f, 0.3f),
            Faction.Purple => new Color(0.8f, 0.3f, 1f),
            Faction.Orange => new Color(1f, 0.6f, 0.2f),
            Faction.Teal => new Color(0.2f, 0.8f, 0.8f),
            Faction.White => new Color(0.9f, 0.9f, 0.9f),
            Faction.Curse => new Color(0.6f, 0.85f, 0.95f), // icy cyan — crystal aesthetic
            _ => Color.gray
        };
    }

